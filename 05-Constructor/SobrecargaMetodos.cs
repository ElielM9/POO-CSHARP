using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Constructor
{
    class Matematicas
    {
        public int Suma(int a, int b)
        {
            int resultado;
            resultado = a + b;

            return resultado;
        }
        public double Suma(double a, double b)
        {
            double resultado;
            resultado = a + b;

            return resultado;
        }

        public int Suma(int a, int b, int c)
        {
            int resultado;
            resultado = a + b + c;

            return resultado;
        }

    }
}

